    public void setTable() throws SQLException {
        CategoryDAO categoryDAO = new CategoryDAOImpl();
        tableCategoryFrame.setModel(buildTableModel(categoryDAO.viewAllCategoryResultSet()));
    }

    public static DefaultTableModel buildTableModel(ResultSet rs) throws SQLException {
        ResultSetMetaData metaData = rs.getMetaData();
        Vector<String> columnNames = new Vector<String>();
        int columnCount = metaData.getColumnCount();
        for (int column = 1; column <= columnCount; column++) {
            columnNames.add(metaData.getColumnName(column));
        }

        Vector<Vector<Object>> data = new Vector<Vector<Object>>();
        while (rs.next()) {
            Vector<Object> vector = new Vector<Object>();
            for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                vector.add(rs.getObject(columnIndex));
            }
            data.add(vector);
        }
        return new DefaultTableModel(data, columnNames);
    }
    
    public void addForOneUpdateForTwo(int one) {
        String categoryName = txtCategoryName.getText();
        String description = txtAreaDescription.getText();

        int createdBy = UserDAOImp.userId;
    
        Date d = new Date();
        SimpleDateFormat df = new SimpleDateFormat();
        df.applyPattern("yyyy-MM-dd");

        String currentDate = df.format(d);

        CategoryBean categoryBean = new CategoryBean();
        categoryBean.setCategoryName(categoryName);
        categoryBean.setDescription(description);
           
        CategoryDAO categoryDAO = new CategoryDAOImpl();
        if (one == 1) {
            if (categoryDAO.checkCategory(categoryBean)) {
                JOptionPane.showMessageDialog(this, "Category already exits!");
            } else {
                categoryBean.setCreatedBy(createdBy);
                categoryBean.setCreatedDate(currentDate);
                
                if (categoryDAO.addCategoryDetails(categoryBean) != 0) {
                    try {
                        clearAll();
                        setTable();
                        JOptionPane.showMessageDialog(this, "Category added successfully!");
                    } catch (SQLException ex) {
                        Logger.getLogger(CategoryFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to add the category!");
                }
            }
        } else if(one==2) {
            int id = (int) tableCategoryFrame.getValueAt(tableCategoryFrame.getSelectedRow(), 0);
            
            categoryBean.setModifiedBy(createdBy);
            categoryBean.setModifiedDate(currentDate);
            categoryBean.setCategoryiD(id);
            
            if (categoryDAO.updateCategoryDetails(categoryBean) != 0) {
                try {
                    clearAll();
                    setTable();
                    setButtons(2);
                    JOptionPane.showMessageDialog(this, "Category updated successfully!");
                } catch (SQLException ex) {
                    Logger.getLogger(CategoryFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update the category!");
            }
        }else if(one==3){
                int id = (int) tableCategoryFrame.getValueAt(tableCategoryFrame.getSelectedRow(), 0);
                categoryBean.setCategoryiD(id);
                if (categoryDAO.deleteCategoryDetails(categoryBean) != 0) {
                try {
                    clearAll();
                    setTable();
                    setButtons(2);
                    JOptionPane.showMessageDialog(this, "Category deleted successfully!");
                } catch (SQLException ex) {
                    Logger.getLogger(CategoryFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the category!");
            }
        }
    }
