    public void setAllTxts() {
        setButtons(1);
        int id = (int) tableCategoryFrame.getValueAt(tableCategoryFrame.getSelectedRow(), 0);
        CategoryBean categoryBean = new CategoryBean();
        categoryBean.setCategoryiD(id);
        
        CategoryDAO categoryDAO = new CategoryDAOImpl();
        ArrayList<CategoryBean> list = (ArrayList<CategoryBean>) categoryDAO.setAllTxt(categoryBean);
        for (CategoryBean categoryBean1 : list) {
            txtCategoryName.setText(categoryBean1.getCategoryName());
            txtAreaDescription.setText(categoryBean1.getDescription());
        }
    }
