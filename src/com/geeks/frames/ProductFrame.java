    public void setAllTxts() {
        setButtons(1);
        int id = (int) tableProductFrame.getValueAt(tableProductFrame.getSelectedRow(), 0);
        ProductBean productBean = new ProductBean();
        productBean.setProductID(id);
        
        ProductDAO productDAO = new ProductDAOImpl();
        ArrayList<ProductBean> list = (ArrayList<ProductBean>) productDAO.setAllTxt(productBean);
        for (ProductBean productBean1 : list) {
            txtProductName.setText(productBean1.getProductName());
            comboCategory.setSelectedItem(productBean1.getCategoryBean().getCategoryName());
            txtPrice.setText(String.valueOf(productBean1.getPrice()));
            txtQuatity.setText(String.valueOf(productBean1.getQuantity()));
            txtSize.setText(productBean1.getSize());
            lblBarCode.setText(productBean1.getBarcode());
        }
    }
    
