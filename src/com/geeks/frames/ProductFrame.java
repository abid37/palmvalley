    public void setTable() throws SQLException {
        ProductDAO categoryDAO = new ProductDAOImpl();
        tableProductFrame.setModel(buildTableModel(categoryDAO.viewAllProductResultSet()));
    }

    public static DefaultTableModel buildTableModel(ResultSet rs) throws SQLException {
        ResultSetMetaData metaData = rs.getMetaData();
        Vector<String> columnNames = new Vector<String>();
        int columnCount = metaData.getColumnCount();
        for (int column = 1; column <= columnCount; column++) {
            columnNames.add(metaData.getColumnName(column));
        }

        Vector<Vector<Object>> data = new Vector<Vector<Object>>();
        while (rs.next()) {
            Vector<Object> vector = new Vector<Object>();
            for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                vector.add(rs.getObject(columnIndex));
            }
            data.add(vector);
        }
        return new DefaultTableModel(data, columnNames);
    }
    
    public void addForOneUpdateForTwo(int one) throws SQLException {
        String productName = txtProductName.getText();
        String categoryName = comboCategory.getSelectedItem().toString();
        String n=txtPrice.getText();
        
        float price = 0;
        if(n.equals("")){
            price = 0;
        }else{
            price = Float.parseFloat(n);
        }
        
        String size = txtSize.getText();
        String barcode = lblBarCode.getText();
         
        String p = txtQuatity.getText();
        int quantity=0;
        if(p.equals("")){
            quantity = 0;
        }else{
            quantity = Integer.parseInt(p);
        }
        
        int createdBy = UserDAOImp.userId;
    
        Date d = new Date();
        SimpleDateFormat df = new SimpleDateFormat();
        df.applyPattern("yyyy-MM-dd");

        String currentDate = df.format(d);

        ProductBean productBean = new ProductBean();
        productBean.setProductName(productName);
            CategoryBean categoryBean = new CategoryBean();
            categoryBean.setCategoryName(categoryName);
        ProductDAO productDAO = new ProductDAOImpl();
            int categoryId = productDAO.getIdByCategory(categoryBean);
            categoryBean.setCategoryiD(categoryId);
        
        productBean.setCategoryBean(categoryBean);
        
        productBean.setPrice(price);
        productBean.setSize(size);
        productBean.setQuantity(quantity);
        setBarCode();
        productBean.setBarcode(barcode);
       
        if (one == 1) {
            if (productDAO.checkProduct(productBean)) {
                JOptionPane.showMessageDialog(this, "Product already exits!");
            } else {
                productBean.setCreatedBy(createdBy);
                productBean.setCreatedDate(currentDate); 
                if (productDAO.addProductDetails(productBean) != 0) {
                clearAll();
                setTable();
                JOptionPane.showMessageDialog(this, "Product added successfully!");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to add the product!");
                } 
            }  
        } else if(one==2) {
            int id = (int) tableProductFrame.getValueAt(tableProductFrame.getSelectedRow(), 0);
            
            productBean.setModifiedBy(createdBy);
            productBean.setModifiedDate(currentDate);
            productBean.setProductID(id);
            
            if (productDAO.updateProductDetails(productBean) != 0) {
                clearAll();
                setTable();
                setButtons(2);
                JOptionPane.showMessageDialog(this, "Product updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update the product!");
            }
        }else if(one==3){
                int id = (int) tableProductFrame.getValueAt(tableProductFrame.getSelectedRow(), 0);
                productBean.setProductID(id);
                if (productDAO.deleteProductDetails(productBean) != 0) {
                    clearAll();
                    setTable();
                    setButtons(2);
                    JOptionPane.showMessageDialog(this, "Product deleted successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the product!");
            }
        }
    }
    
    public void setComboCategory(){
        ProductDAO productDAO = new ProductDAOImpl();
        List<String> li = productDAO.viewAllCategory();
        comboCategory.addItem("Select one");
        for(String s : li){
            comboCategory.addItem(s);
        }
    }
